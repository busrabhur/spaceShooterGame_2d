
public class Ates {
    //ateşlerin x ,y koordinatları var ve actionPerformed her çalıştığında ateşimizin yukarı doğru ilerlemesi gerekiyor.
    private int x;
    private int y;
    
    //constructor ve getter setterlarımızı yazalım

    public Ates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
    
    
    
    
    
}
